<b>May 20, 2009: Our HPC cluster featured in CIS webpage</b> 
<br>Our group HPC Linux Cluster "Geronimo" from <a target="_blank" href="http://www.penguincomputing.com/">Penguin Computing</a> is featured in the <a target="_blank" href="http://www.cis.udel.edu">CIS webpage</a>. Geronimo has a triple NVIDIA Tesla S1070 making use of GPUs (Graphics Processing Units) for scientific computation.  Geronimo's 12 GPU's deploy 2880 processing cores and there are an additional 48 conventional CPU's. The floating point peak performance is 12.4 teraflops per second.  For comparison, the largest Beowulf clusters on campus have peak performance of about 2 teraflops per second.  Our group will use Geronimo to design, implement, and test high performance parallel algorithms for molecular modeling.

